package day03;

public class Ex03Opr {

	public static void main(String[] args) {

		// 4. 비교 연산자
		// 두 개의 값을 비교 - > 결과를 boolean 자료형으로  리턴
		
		System.out.println(2 < 4); //true //2가 4보다 작니?->응
		System.out.println(3 < 3); //false
		System.out.println(3 <= 3);//true
		System.out.println(10 > 20); //false
		
		System.out.println(10 == 10); // true
		System.out.println(10 != 10); // false //10과 10이 다르니?->아니
		
		
		// 비교 연산자의 결과 : boolean
	}

}
