package day01;

public class Ex02Var {

	public static void main(String[] args) {

		//1. 변수(Variable)
		
		// 한 줄 이동 : Alt + 방향키 위/아래
		// 한 줄 복사 : Ctrl + Alt + 방향키 위/아래
		
		int box = 100;

		//Q. 3이라는 값을 정수형 변수 box2에 대입해주세요
		int box2 = 3; //세미콜론은 컴파일 언어(전체파일을 통으로 번역)의 특징!문장 구분을 위해 세미콜론사용 필요)
				
		System.out.println(box + box2); //10
		System.out.println(box - box2); //4
		System.out.println(box * box2); //21 (* : asterisk, 아스타리카)
		System.out.println(box / box2); //2 (몫 연산자)
		
		
	}
		

}
